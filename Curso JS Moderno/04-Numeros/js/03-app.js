//! Métodos Math

//! PI
Math.PI;

//! Redondeo
Math.round(2.8); // 3
Math.round(2.2); // 2
Math.round(2.5); // 3

// Redondeo superior
Math.ceil(2.2); // 3

// Redondeo inferior
Math.floor(2.8); // 2

//! Raíz cuadrada
Math.sqrt(144); // 12

//! Valor absoluto
Math.abs(-3); // 3

//! Potencia
Math.pow(2, 3); // 8

//! Número mínimo y máximo
Math.min(2, 3, 5, 1, 6); // 1
Math.max(2, 3, 5, 1, 6); // 6

//! Número aleatorio
Math.random(); // Entre 0 y 1
Math.floor(Math.random() * 30); // Entero del 0 al 29
